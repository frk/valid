package tables

import (
	"regexp"
	"strings"
)

var ISO_639_1 = make(map[string]Language)
var ISO_639_2 = make(map[string]Language)
var Alpha = make(map[string]*regexp.Regexp)
var Alnum = make(map[string]*regexp.Regexp)

func init() {
	for _, lang := range languages {
		// set of ISO 639‚Äë2 (three letter codes)
		ISO_639_2[lang.ISO_639_2] = lang

		// set of ISO 639‚Äë1 (two letter codes)
		if lang.ISO_639_1 != "" {
			ISO_639_1[lang.ISO_639_1] = lang
		}

		abc := strings.ReplaceAll(lang.Alphabet, " ", "")
		num := strings.ReplaceAll(lang.Numerals, " ", "")
		if len(abc) > 0 {
			// Alpha
			rxabc := regexp.MustCompile(`^(?i)[` + abc + `]+$`)
			Alpha[lang.ISO_639_2] = rxabc
			if lang.ISO_639_1 != "" {
				Alpha[lang.ISO_639_1] = rxabc
			}

			// Alnum
			rxnum := regexp.MustCompile(`^(?i)[` + abc + num + `]+$`)
			Alnum[lang.ISO_639_2] = rxnum
			if lang.ISO_639_1 != "" {
				Alnum[lang.ISO_639_1] = rxnum
			}
		}

	}
}

