package tables

import (
	"regexp"
	"strings"
)

var ISO639_1 = make(map[string]Language)
var ISO639_2 = make(map[string]Language)
var Alpha = make(map[string]*regexp.Regexp)
var Alnum = make(map[string]*regexp.Regexp)

func init() {
	for _, lang := range languages {
		// set of ISO 639‚Äë2 (two letter codes)
		ISO639_2[lang.Code3] = lang

		// set of ISO 639‚Äë1 (two letter codes)
		if lang.Code2 != "" {
			ISO639_1[lang.Code2] = lang
		}

		abc := strings.ReplaceAll(lang.Alphabet, " ", "")
		num := strings.ReplaceAll(lang.Numerals, " ", "")
		if len(abc) > 0 {
			// Alpha
			rxabc := regexp.MustCompile(`^(?i)[` + abc + `]+$`)
			Alpha[lang.Code3] = rxabc
			if lang.Code2 != "" {
				Alpha[lang.Code2] = rxabc
			}

			// Alnum
			rxnum := regexp.MustCompile(`^(?i)[` + abc + num + `]+$`)
			Alnum[lang.Code3] = rxnum
			if lang.Code2 != "" {
				Alnum[lang.Code2] = rxnum
			}
		}

	}
}

